package classes;

public class Area {
    private int idArea;
    private String nomeArea;
    
    public void setIdArea(int idArea){
        this.idArea = idArea;
    }
    
    public void setNomeArea(String nomeArea){
        this.nomeArea = nomeArea;
    }
    
    public int getIdArea(){
        return idArea;
    }
    
    public String getNomeArea(){
        return nomeArea;
    }
}
