package classes;

public class AgenciaFomento {
    private int idAgFomento;
    private String nomeAgFomento;
    
    public void setIdAgFomento(int idAgFomento){
        this.idAgFomento = idAgFomento;
    }
    
    public void setNomeAgFomento(String nomeAgFomento){
        this.nomeAgFomento = nomeAgFomento;
    }
    
    public int getIdAgFomento(){
        return idAgFomento;
    }
    
    public String getNomeAgFomento(){
        return nomeAgFomento;
    }
    
}
